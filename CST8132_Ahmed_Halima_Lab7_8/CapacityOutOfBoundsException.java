public class CapacityOutOfBoundsException extends RuntimeException {

	/***
	 * The purpose of this class is for the CapacityOutOfBoundsException class to
	 * extend RuntimeException
	 * 
	 * @author Halima Ahmed
	 * @version 1.0
	 * @since 1.8
	 */

	CapacityOutOfBoundsException() {
		/** Calls default super constructor **/
		super();
	}

	CapacityOutOfBoundsException(String s) {
		/** Initial constructor. Passes s to the super class constructor. **/
		super(s);
	}
}
